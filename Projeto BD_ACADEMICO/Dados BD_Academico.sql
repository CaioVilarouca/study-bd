SELECT T.DESCRICAO          as NOME_TURMA,
	   T.DT_INICIAL         as DATA_INICIO_TURMA,  
       T.DT_FINAL           as DATA_TERMINO_TURMA,
	   P.NOME               as NOME_PROFESSOR,
       D.NO_DISCIPLINA      as NOME_DISCIPLINA
FROM TURMA          as T,
     PROF_TURM_DISC as PTD ,
     PROFESSOR      as P,
     DISCIPLINA     as D
WHERE T.CO_TURMA = PTD.CO_TURMA
  AND P.CO_PROFESSOR = PTD.CO_PROFESSOR
  AND D.CO_DISCIPLINA = PTD.CO_DISCIPLINA
ORDER BY T.CO_TURMA;







